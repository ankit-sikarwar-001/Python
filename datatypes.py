# 1. Primitive (or Built-in) Data Types
# These are basic data types provided by the programming language:

# Numeric Types:
# Integer (int): Whole numbers (e.g., 1, 42, -10).
# Float (float): Numbers with decimal points (e.g., 3.14, -0.5).
# Complex (complex): Numbers with real and imaginary parts (e.g., 2 + 3j in Python).
# Text Type:
# String (str): Sequence of characters (e.g., "hello", "123").
# Boolean (bool):
# Logical values: True or False.
# Character (char) (in some languages like C):
# Single character (e.g., 'A', '9').
# 2. Derived (or Composite) Data Types
# These are data types built from primitive types:

# List/Array:
# Collection of elements (e.g., [1, 2, 3] or ["a", "b", "c"]).
# Tuple:
# Immutable sequence of elements (e.g., (1, 2, 3) in Python).
# Set:
# Collection of unique, unordered elements (e.g., {1, 2, 3} in Python).
# Dictionary/HashMap:
# Key-value pairs (e.g., {"name": "Ankit", "age": 22} in Python).




